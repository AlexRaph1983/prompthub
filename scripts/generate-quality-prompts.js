  console.log(`\nüéâ –°–û–•–†–ê–ù–ï–ù–ò–ï –ó–ê–í–ï–†–®–ï–ù–û! –î–æ–±–∞–≤–ª–µ–Ω–æ ${savedCount} –∫–∞—á–µ—Å—Ç–≤–µ–Ω–Ω—ã—Ö –ø—Ä–æ–º–ø—Ç–æ–≤`);

